/** \brief Realiza la suma entre dos numeros.
 *
 * \param  Representa un numero de tipo flotante.
 * \param  Representa un numero de tipo flotante,
 * \return Valor de tipo flotante que representa el resultado de la suma.
 *
 */
float sumar(float num1, float num2);

/** \brief Realiza la resta entre dos numeros.
 *
 * \param Representa un numero de tipo flotante.
 * \param Representa un numero de tipo flotante.
 * \return Valor de tipo flotante que representa el resultado de la resta.
 *
 */
float restar(float num1, float num2);

/** \brief Realiza la division entre dos numeros.
 *
 * \param Representa un numero de tipo flotante
 * \param Representa un numero de tipo flotante
 * \return Valor de tipo flotante que representa el resultado de la division.
 *
 */
float dividir(float num1, float num2);

/** \brief Realiza la multiplicacion entre dos numeros.
 *
 * \param Representa un numero de tipo flotante
 * \param Representa un numero de tipo flotante
 * \return Valor de tipo flotante que representa el resultado de la multiplicacion
 *
 */
float multiplicar(float num1, float num2);

/** \brief Realiza el factorial de un numero.
 *
 * \param Representa un numero de tipo entero.
 * \return Valor de tipo long long int que representa el factorial de un numero menor o igual a 20.
 *
 */
long long int factorial (int num);
